#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	i;

	i = 0;
	while ((((char *)s)[i]) != '\0')
	{
		f(i, s + i);
		i++;
	}
	(((char *)s)[i]) = '\0';
}
